// ─── PAYMENT SPLITS ───
const gstRate = 18;
function makeSplit(id: string, firmId: string, planId: string, projId: string, num: number, amount: number, trigger: 'date' | 'milestone', triggerDate: string | undefined, milestoneId: string | undefined, status: PaymentSplit['status']): PaymentSplit {
  const gstAmount = Math.round(amount * gstRate / 100);
  return {
    id, firm_id: firmId, payment_plan_id: planId, project_id: projId,
    split_number: num, amount, trigger_type: trigger,
    trigger_date: triggerDate, trigger_milestone_id: milestoneId,
    status, gst_rate: gstRate, gst_amount: gstAmount,
    total_with_gst: amount + gstAmount,
    created_at: '2024-03-20T10:00:00Z',
  };
}

